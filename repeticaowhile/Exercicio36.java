package repeticaowhile;
import java.util.Scanner;

public class Exercicio36 {
    /*36 - Faça um programa que exiba os números de 1 a 10 usando o loop "while".*/
    public static void main(String[] args) {
        int n = 1;

        while (n <= 10) {
            System.out.println(n);
            n++;
        }
    }
}
