package repeticaofor;

public class Exercicio66 {
    /*66) Faça um programa que exiba os números de 1 a 10.*/
    public static void main(String[] args) {

        System.out.println("Números de 1 a 10:");
        for (int n = 1; n <= 10; n++) {
            System.out.println(n);
        }
    }
}
